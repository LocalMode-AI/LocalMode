/**
 * Evaluation Types
 *
 * Type definitions for the evaluation SDK: metric functions, datasets,
 * orchestrator options/results, and confusion matrices.
 *
 * @packageDocumentation
 */

/**
 * A metric function that scores predictions against expected labels.
 *
 * All built-in metric functions (`accuracy`, `f1Score`, `precision`, `recall`, etc.)
 * conform to this type. You can also create custom metric functions.
 *
 * @typeParam TPrediction - The type of predicted values
 * @typeParam TLabel - The type of expected label values
 *
 * @example
 * ```ts
 * import type { MetricFunction } from '@localmode/core';
 *
 * const myMetric: MetricFunction<string, string> = (predictions, labels) => {
 *   let correct = 0;
 *   for (let i = 0; i < predictions.length; i++) {
 *     if (predictions[i] === labels[i]) correct++;
 *   }
 *   return correct / predictions.length;
 * };
 * ```
 */
export type MetricFunction<TPrediction = unknown, TLabel = unknown> = (
  predictions: TPrediction[],
  labels: TLabel[],
) => number;

/**
 * Options for the `evaluateModel()` orchestrator function.
 *
 * @typeParam TInput - The type of dataset inputs
 * @typeParam TOutput - The type of model outputs and expected values
 */
export interface EvaluateModelOptions<TInput, TOutput> {
  /** Test dataset with inputs and expected outputs */
  dataset: {
    /** Array of input values to feed to the model */
    inputs: TInput[];
    /** Array of expected output values (ground truth) */
    expected: TOutput[];
  };

  /**
   * Prediction function that wraps the model call.
   * Called once per input with an AbortSignal for cancellation.
   */
  predict: (input: TInput, signal: AbortSignal) => Promise<TOutput>;

  /**
   * Metric function to score predictions against expected values.
   * Called once after all predictions are collected.
   */
  metric: (predictions: TOutput[], expected: TOutput[]) => number;

  /** Optional AbortSignal for cancelling the evaluation */
  abortSignal?: AbortSignal;

  /**
   * Progress callback invoked after each prediction.
   *
   * @param completed - Number of predictions completed so far
   * @param total - Total number of predictions to make
   */
  onProgress?: (completed: number, total: number) => void;
}

/**
 * Result from the `evaluateModel()` orchestrator function.
 *
 * @typeParam TOutput - The type of model outputs
 */
export interface EvaluateModelResult<TOutput> {
  /** The computed metric score */
  score: number;

  /** Array of predictions generated by the model */
  predictions: TOutput[];

  /** Number of items in the dataset */
  datasetSize: number;

  /** Total evaluation duration in milliseconds */
  durationMs: number;
}

/**
 * A structured confusion matrix for classification evaluation.
 *
 * The matrix is a 2D array where `matrix[i][j]` is the count of samples
 * with true label `labels[i]` predicted as `labels[j]`. Labels are sorted
 * alphabetically.
 *
 * @example
 * ```ts
 * import { confusionMatrix } from '@localmode/core';
 *
 * const cm = confusionMatrix(
 *   ['cat', 'dog', 'cat', 'cat'],
 *   ['cat', 'dog', 'dog', 'cat'],
 * );
 *
 * console.log(cm.labels);           // ['cat', 'dog']
 * console.log(cm.matrix);           // [[2, 1], [0, 1]]
 * console.log(cm.truePositives('cat'));  // 2
 * console.log(cm.falsePositives('cat')); // 0
 * ```
 */
export interface ConfusionMatrix {
  /** 2D count matrix where `matrix[i][j]` = count of true `labels[i]` predicted as `labels[j]` */
  matrix: number[][];

  /** Sorted unique labels defining row/column order */
  labels: string[];

  /**
   * Count of correct predictions for the given label.
   * Equals `matrix[idx][idx]` where `idx` is the label's index.
   *
   * @param label - The class label
   * @returns Number of true positives
   */
  truePositives(label: string): number;

  /**
   * Count of incorrect predictions of the given label.
   * Sum of column `idx` minus the diagonal entry.
   *
   * @param label - The class label
   * @returns Number of false positives
   */
  falsePositives(label: string): number;

  /**
   * Count of items correctly not predicted as the given label.
   * Total items minus TP, FP, and FN for the label.
   *
   * @param label - The class label
   * @returns Number of true negatives
   */
  trueNegatives(label: string): number;

  /**
   * Count of items that were the given label but predicted as something else.
   * Sum of row `idx` minus the diagonal entry.
   *
   * @param label - The class label
   * @returns Number of false negatives
   */
  falseNegatives(label: string): number;
}
